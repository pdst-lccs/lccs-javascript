let country = prompt("Enter a country and I will tell you its capital");

if (country == "Ireland") {
   console.log("Dublin");
} else if (country == "Scotland") {
   console.log("Edinburgh");
} else if (country == "England") {
   console.log("London");
} else if (country == "Wales") {
   console.log("Cardiff");
} else if (country == "France") {
   console.log("Paris");
}
